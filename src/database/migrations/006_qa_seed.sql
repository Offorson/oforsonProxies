-- ---------------------------------------------------------
-- 4. Bandwidth usage (30 days of hourly-ish rollups)
--    The dashboard groups by day, so per-day buckets are enough.
-- ---------------------------------------------------------
do $$
declare
  d int;
  uid uuid;
  ptype proxy_type;
  base bigint;
  jitter bigint;
  users record;
begin
  for users in
    select user_id, proxy_type from public.subscriptions
  loop
    uid := users.user_id;
    ptype := users.proxy_type;
    -- datacenter users move way more traffic than residential
    base := case
      when ptype = 'datacenter' then 60_000_000_000   -- 60 GB / day
      when ptype = 'static_residential' then 18_000_000_000
      else 12_000_000_000
    end;

    for d in 0..29 loop
      jitter := (random() * base * 0.4)::bigint - (base * 0.2)::bigint;
      insert into public.bandwidth_usage (user_id, bytes_used, proxy_type, recorded_at)
      values (
        uid,
        greatest(base + jitter, 100_000_000),
        ptype,
        date_trunc('day', now()) - (d || ' days')::interval + (floor(random()*23) || ' hours')::interval
      );
    end loop;
  end loop;

  -- Push the capped user into clear overage territory today
  insert into public.bandwidth_usage (user_id, bytes_used, proxy_type, recorded_at)
  values (
    '66666666-6666-6666-6666-666666666666',
    80_000_000_000,  -- +80 GB spike today
    'rotating_residential',
    now() - interval '15 minutes'
  );
end$$;

