-- 0. Clean previous QA data (idempotent re-runs)
-- ---------------------------------------------------------
do $$
declare
  qa_ids uuid[];
begin
  select array_agg(id) into qa_ids
  from auth.users
  where email like '%@qa.oforson.test';

  if qa_ids is not null then
    delete from public.bandwidth_usage     where user_id = any(qa_ids);
    delete from public.proxy_sessions      where user_id = any(qa_ids);
